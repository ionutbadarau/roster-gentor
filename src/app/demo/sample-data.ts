const pad = (n: number) => String(n).padStart(2, '0');
const dateStr = (day: number) => `${DEMO_YEAR}-${pad(DEMO_MONTH + 1)}-${pad(day)}`;

const buildShifts = (): Shift[] => {
  const out: Shift[] = [];
  for (const [doctorId, entries] of Object.entries(demoShiftPlan)) {
    for (const [day, type] of entries) {
      out.push({
        id: `demo-shift-${doctorId}-${dateStr(day)}-${type}`,
        doctor_id: doctorId,
        shift_date: dateStr(day),
        shift_type: type,
        // Mark one Dr. Ana shift as a manual override so the yellow ring shows in the demo.
        is_manual: doctorId === 'demo-doc-1' && day === 17 && type === 'day',
      });
    }
  }
  return out;
};

export const demoShifts: Shift[] = buildShifts();

