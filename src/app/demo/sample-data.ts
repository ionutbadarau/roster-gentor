// Precomputed per-doctor stats matching the ShiftGridDoctorRow `DoctorStats` shape.
export const demoDoctorStats: Record<string, { dayShifts: number; nightShifts: number; totalHours: number; baseNorm: number }> =
  Object.fromEntries(
    demoDoctors.map((d) => {
      const docShifts = demoShifts.filter((s) => s.doctor_id === d.id);
      const dayShifts = docShifts.filter((s) => s.shift_type === 'day').length;
      const nightShifts = docShifts.filter((s) => s.shift_type === 'night').length;
      return [
        d.id,
        {
          dayShifts,
          nightShifts,
          totalHours: (dayShifts + nightShifts) * 12,
          baseNorm: d.is_optional ? 0 : DEMO_BASE_NORM_HOURS,
        },
      ];
    })
  );
